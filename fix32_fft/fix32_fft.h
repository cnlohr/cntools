int fix_fft(int32_t fr[], int32_t fi[], int m, int inverse)
{
	// Compute _n_ from _m_
	int n = 1 << m;

	/* max FFT size = N_WAVE */
	if (n > N_WAVE)
		return -1;

	int mr, nn, i, j, l, k, istep;
	int32_t qr, qi, tr, ti, wr, wi;

	mr = 0;
	nn = n - 1;

	/* decimation in time - re-order data */
	for (m=1; m<=nn; ++m) {
		l = n;
		do {
			l >>= 1;
		} while (mr+l > nn);
		mr = (mr & (l-1)) + l;

		if (mr <= m)
			continue;
		tr = fr[m];
		fr[m] = fr[mr];
		fr[mr] = tr;

		ti = fi[m];
		fi[m] = fi[mr];
		fi[mr] = ti;
	}

	l = 1;
	k = LOG2_N_WAVE-1;
	while (l < n) {
		/*
		  it may not be obvious, but the shift will be
		  performed on each data point exactly once,
		  during this pass.
		*/
		istep = l << 1;
		for (m=0; m<l; ++m)
		{
			j = m << k;
			/* 0 <= j < N_WAVE/2 */

			//printf( "%d\n", j );
//			wr =  Sinewave[j+N_WAVE/4]<<16;
//			wi = -Sinewave[j]<<16;
			printf( "%d / %d %d\n", j, m, istep );

			if (inverse)
			{
				wi = -wi;
			}
			else
			{
				// Shift
				wr >>= 1;
				wi >>= 1;
			}
			for (i=m; i<n; i+=istep) {
				j = i + l;
				tr = FIX_MPY(wr,fr[j]) - FIX_MPY(wi,fi[j]);
				ti = FIX_MPY(wr,fi[j]) + FIX_MPY(wi,fr[j]);
				qr = fr[i];
				qi = fi[i];
				if (!inverse) {
					// Shift
					qr >>= 1;
					qi >>= 1;
				}
				fr[j] = qr - tr;
				fi[j] = qi - ti;
				fr[i] = qr + tr;
				fi[i] = qi + ti;
			}
		}
		--k;
		l = istep;
	}
	return 0;
}

int fix_fftr(int32_t f[], int m, int inverse)
{
	int i, N = 1<<(m-1), ret = 0;
	int32_t tt, *fr=f, *fi=&f[N];

	if (inverse)
	{
		ret = fix_fft(fi, fr, m-1, inverse);
	}
	for (i=1; i<N; i+=2)
	{
		tt = f[N+i-1];
		f[N+i-1] = f[i];
		f[i] = tt;
	}
	if (! inverse)
	{
		ret = fix_fft(fi, fr, m-1, inverse);
	}
	return ret;
}

