static inline int32_t FIX_MPY(int32_t a, int32_t b)
{
#ifdef FIX32_FFT_PRECISEROUNDING
	int32_t c = ((int64_t)a * (int64_t)b) >> 30;
	// last bit shifted out = rounding-bit, from previous implementation
	b = c & 0x01;
	// last shift + rounding bit
	return (c >> 1) + b;
#else
	return ((int64_t)a * (int64_t)b) >> 31;
#endif
}


